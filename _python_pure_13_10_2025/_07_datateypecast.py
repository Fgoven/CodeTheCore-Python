
# Type Casting veri türlerinin birbirine dönüştürülmesi

# int()
# str()
# float()
# bool()

number1 ="44"
print(f"Sayı {number1} ==> type{type(number1)}")
number1 = int(number1)
print(f"Sayı {number1} ==> type{type(number1)}")
number1 = float(number1)
print(f"Sayı {number1} ==> type{type(number1)}")
x = "True"
number1 = bool(x)
print(f"Sayı {number1} ==> type{type(number1)}")


